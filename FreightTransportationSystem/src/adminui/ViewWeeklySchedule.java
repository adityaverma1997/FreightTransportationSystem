    private void btnCheckScheduleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckScheduleActionPerformed
        ProSound ps = new ProSound();
        String message = "";
        try {
            if (fdpTourDate.getSelectedDate() != null) {
                tableWeeklySchedule.setVisible(true);
                btnLoad.setEnabled(true);
                ps.button();
                bindTables();
            } else {
                ps.computererror();
                tableWeeklySchedule.setVisible(false);
                btnLoad.setEnabled(false);
                btnReport.setEnabled(false);
                message += "Please choose any date";
                JOptionPane.showMessageDialog(this, message);
            }

        } catch (Exception ex) {

        }
    }//GEN-LAST:event_btnCheckScheduleActionPerformed

