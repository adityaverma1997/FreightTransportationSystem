    public void bindTables() {
        List<TourSchedule> schedule = new TourScheduleDAO().getAllRecord(fdpTourDate.getSelectedDate());
        String message = "";
        String[] colnames = {"TRUCK NO.", "DRIVER NAME", "TOUR DATE", "NO. OF DAYS"};
        Object[][] records = null;
        if (schedule != null && schedule.size() > 0) {
            btnReport.setEnabled(true);
            message += "Weekly schedule is as shown in the given table.";
            records = new Object[schedule.size()][colnames.length];
            int i = 0;
            for (TourSchedule ts : schedule) {
                records[i] = new Object[]{ts.getTruckno(), ts.getDrivername() + "[" + ts.getDriverid() + "]", ts.getTourdate(), ts.getNoofdays()};
                i++;
            }
        } else {
            btnReport.setEnabled(false);
            message += "There is no weekly schedule available for this week.";
            records = new Object[1][colnames.length];
            records[0] = new Object[]{"No Records", "No Records", "No Records", "No Records"};
        }
        DefaultTableModel dtm = new DefaultTableModel(records, colnames) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }

        };
        tableWeeklySchedule.getTableHeader().setReorderingAllowed(false);
        tableWeeklySchedule.setAutoCreateRowSorter(true);
        tableWeeklySchedule.setModel(dtm);
        JOptionPane.showMessageDialog(this, message);
    }

